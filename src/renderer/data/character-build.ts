// Mechanical reference for player character building: races (ability score
// increases + racial traits), classes (HD, saves, key features), feats and
// backgrounds. Distinct from data/races.ts which holds lore-only fields.
//
// Seeded with a few canonical PHB entries so the page is useful out of the box;
// extend by appending to the arrays below.

export interface BuildTrait {
  /** Short name, e.g. "Тёмное зрение", "Дополнительная атака". */
  name: string
  /** Mechanical description. Markdown is not parsed here; «спелл» refs and
   *  dice notation will be rendered via DiceText. */
  desc: string
  /** Class feature unlocks at this level (1-20). Omit for race/feat/background. */
  level?: number
}

export interface RaceBuild {
  id: string
  name: string
  /** Ability score increases, e.g. "+1 ко всем характеристикам". */
  asi: string
  size: string
  /** Walking speed in feet (other speeds listed in traits). */
  speed: number
  langs: string
  traits: BuildTrait[]
  /** Optional subraces, e.g. Высший эльф / Лесной эльф. */
  subraces?: { name: string; asi: string; traits: BuildTrait[] }[]
  source?: string
}

export interface ClassBuild {
  id: string
  name: string
  /** Hit die in "к"-notation, e.g. "к10". */
  hitDie: string
  /** Primary ability for attack/spell rolls. */
  primary: string
  /** Two saving throw proficiencies. */
  saves: string
  armor: string
  weapons: string
  tools?: string
  /** Skills text, e.g. "Две на выбор из: Атлетика, Запугивание…". */
  skills: string
  /** Iconic features, ordered by level. */
  features: BuildTrait[]
  /** Optional subclasses (Первобытные пути, Воинские архетипы, Колдовские покровители…).
   *  Each subclass lists the features it grants at its unlock levels and may
   *  carry its own caster progression and expanded-spell list. */
  subclasses?: {
    name: string
    meta?: string
    features: BuildTrait[]
    /** Subclass-only spell progression: typically `third` for Eldritch Knight
     *  and Arcane Trickster. Renders an extra slot table inside the card. */
    caster?: CasterKind
    /** Spells the subclass grants as «always prepared», keyed by spell level.
     *  Rendered as a compact table inside the subclass card. */
    expandedSpells?: { level: number; spells: string[] }[]
  }[]
  source?: string
}

export interface ProgressionRow {
  level: number
  /** Proficiency bonus at this level (+2 / +3 / +4 / +5 / +6). */
  pb: number
  /** Short comma- or dot-separated list of features gained at this level. */
  features: string
  /** Per-class resource columns indexed by the column header in
   *  ClassProgression.columns (e.g. "Ярости", "Заговоры"). Missing entries
   *  render as "—". */
  cols?: Record<string, string>
}

/** Kind of spell-slot progression. Controls which canonical 5e PH24 spell-slot
 *  table is rendered below the main level chart. `third` is used by subclass-
 *  level casters (Eldritch Knight, Arcane Trickster). */
export type CasterKind = 'full' | 'half' | 'pact' | 'third'

export interface ClassProgression {
  /** Headers of class-specific columns appended after Level, PB, Features. */
  columns: string[]
  rows: ProgressionRow[]
  /** If set, an additional «Ячейки заклинаний» table is rendered. */
  caster?: CasterKind
}

export interface FeatBuild {
  id: string
  name: string
  /** Prerequisite text, e.g. "СИЛ 13 или ЛОВ 13"; empty if none. */
  prereq?: string
  /** Short opening summary. */
  desc: string
  /** Mechanical benefits as bullet items. */
  bonuses: string[]
  source?: string
}

export interface BackgroundBuild {
  id: string
  name: string
  /** Granted skill proficiencies. */
  skills: string
  /** Tool proficiencies, if any. */
  tools?: string
  /** Granted languages, if any. */
  langs?: string
  /** Starting equipment. */
  equipment: string
  /** Named background feature for PHB 2014 entries (e.g. "Укрытие плута"). */
  feature?: BuildTrait
  /** PH24: trio of abilities the background lets the player distribute +2/+1
   *  (or +1/+1/+1) between. */
  abilities?: string
  /** PH24: name of the Origin Feat granted by the background. */
  feat?: string
  /** Personality / ideal / bond / flaw suggestions. */
  suggestion?: string
  /** Narrative lore paragraph — the life this character likely lived. Rendered
   *  as a flavour-text section at the top of the background card. */
  lore?: string
  source?: string
}

