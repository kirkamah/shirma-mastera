// Built-in optional / homebrew rules shown on the «Опциональные правила» page.
// These are static reference content (rendered read-only with rich blocks);
// the user's own rules live alongside them via the `useCustom('rule')` store.
//
// Block text supports the same DiceText markup as the rest of the app:
// dice (2к6+3), DC (СЛ 15), «ссылки», **жирный**, *курсив*, {цвет:текст}, and
// auto-highlighted conditions. Dice inside table cells stay interactive too.

export type RuleBlock =
  | { type: 'p'; text: string }
  | { type: 'h'; text: string }
  | { type: 'list'; items: string[] }
  | { type: 'table'; head: string[]; rows: string[][]; caption?: string }
  | { type: 'note'; text: string }

export interface OptionalRule {
  id: string
  name: string
  /** Sidebar group label. */
  category: string
  /** Short subtitle shown under the title. */
  tag?: string
  /** Marks non-canonical rules with a [Хоумбрю] badge. */
  homebrew?: boolean
  /** Marks rules straight from the official 2024 books with an [Официально] badge. */
  official?: boolean
  blocks: RuleBlock[]
}

