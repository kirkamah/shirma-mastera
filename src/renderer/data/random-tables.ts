// Случайные таблицы: лут, безделушки, награды и т.п. Мастер бросает к20/к100
// и читает строку, в чей диапазон попал результат. Пользователь может создавать
// собственные таблицы (хранятся как custom-контент, см. useCustom).

export interface RandomTableEntry {
  /** Нижняя и верхняя границы диапазона броска (включительно). Для одиночного
   *  значения min === max. */
  min: number
  max: number
  result: string
}

export interface RandomTable {
  id: string
  name: string
  /** Группа в списке: «Лут», «Безделушки», «Награды», «Свои» и т.д. */
  category: string
  /** Тип кубика: 20 или 100. */
  die: number
  desc?: string
  entries: RandomTableEntry[]
  source?: string
}

/** Найти строку таблицы, в чей диапазон попал бросок. */
export function entryForRoll(table: RandomTable, roll: number): RandomTableEntry | undefined {
  return table.entries.find((e) => roll >= e.min && roll <= e.max)
}

// одиночное значение → диапазон [n, n]
const one = (n: number, result: string): RandomTableEntry => ({ min: n, max: n, result })
const range = (min: number, max: number, result: string): RandomTableEntry => ({ min, max, result })

